{
  int i;
  for (i = 0; i < sizeof(coc_table) / sizeof(coc_table_s); i++) {
    if (!make || !strncasecmp (coc_table[i].make, make, strlen (make))) {
      if (!model || strcasestr (coc_table[i].model, model))
	fprintf (stdout, "%4d: \"%s\" \"%s\" %g\n",
		 i,
		 coc_table[i].make,
		 coc_table[i].model,
		 coc_table[i].coc);
    }
  }
}
